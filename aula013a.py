# estrultura de repetição
# ex: for ? in range(1, 10):
# você pode chamar para printar a variavel ali destinada também, sendo assim ela assume o valor de range
# se adicionar mais uma virgula ele conta num intervalo de números ex: for ? in range(1, 10 , 2)
# pra contar ao contrario digite -1

n = int(input('digite um número: '))
for c in range(1, n+1):
    print(f'{c} paçoca')
