# uso do elif
# enquanto o if pode existir sem ninguém, o else só pode existir na presença de if e uma unica vez
# e o elif pode existir quantas vezes você quiser desde que exista um if

nome = input('de um nome a rola: ')

if nome == 'roludo':
    print('otimo nome de rola amigo')
elif nome == 'piroquitos':
    print('bela rola zé piroca')
else:
    print('o meu é maior')

