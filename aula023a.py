# erra o nome de um comando ou quando a variavel não foi declarada direito também
# ValueError: quando recebe um valor que não era esperado
# ZeroDivisionError: quando se tenta dividir algo por 0
# TypeError: quando se tenta usar o tipo errado de variavel para realizar uma ação
# IndexError: quando se tenta mostrar algo ou adicionar em um endereço que não existe em uma lista
# esses são os mais comuns
# para lidar com erros, você pode usar o try: para tentar, se ocorrer algum erro ele não executa
# e o except: faz outra ação se der errado o try
