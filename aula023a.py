# usando o finally:(opicional) ele irá fazer a ação independente se o try der certo ou não
# da pra adicionar ao except: o comando exception que irá dar a causa do problema ex:
# da mesma forma, pode se adicionar o tipo de erro ao except para que ele realize aquela ação
# só pra aquele tipo de erro, já que podem ser adicionados vários except pra um unico try
